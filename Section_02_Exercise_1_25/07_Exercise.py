"""
Exercise No. 7

Create a function that returns True when num1 is equal num2; otherwise return False.

Example

is_same_number(4, 8) -> False

is_same_number(2, 2) -> True

is_same_number(2, "2") -> False
"""


def is_same_number(num1, num2):
    return num1 == num2


print(is_same_number(1, 1))
