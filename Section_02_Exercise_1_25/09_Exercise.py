"""
Exercise No. 9

Create a function that takes a number as its argument and returns True if it's less than or equal to zero, otherwise
return False.

Examples

less_than_or_equal_to_zero(5) -> False

less_than_or_equal_to_zero(0) -> True

less_than_or_equal_to_zero(2) -> True

Notes
Don't forget to return the result.
"""


def less_than_or_equal_to_zero(num):
    return num <= 0


print(less_than_or_equal_to_zero(1))
